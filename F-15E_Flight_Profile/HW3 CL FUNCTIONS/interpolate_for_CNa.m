             0.456332307, 0.475682792, 0.495033276, 0.514383761, 0.533734245, 0.55308473, ...
             0.572435214, 0.591785699, 0.611136184, 0.630486668, 0.649837153, 0.669187637, ...
             0.688538122, 0.707888606, 0.727239091, 0.746589576, 0.76594006, 0.785290545, ...
             0.804641029, 0.823991514, 0.843341998, 0.862692483, 0.882042968, 0.901393452, ...
             0.920743937, 0.940094421, 0.959444906, 0.97879539, 1.000914041];

    betaCNa_data = [3.984093077, 3.988067921, 4.000135683, 3.999660794, 4.000373127, 4.000373127, ...
            4.000135683, 4.000135683, 3.998948461, 4.003934793, 4.006784125, 4.010108346, ...
