    % Nicolai supersonic lift curve slope estimation for taper ratio of 0.25, 
    % and AR*tan(sweep LE) of 3. See Fig. 13.2

    % Lambda_LE should be in radians

    % Define tan_B and B_CN data points
    tanLE_Beta_data = [0.000561192, 0.010951025, 0.030621646, 0.049972131, 0.069322616, 0.0886731, ...
             0.108023585, 0.127374069, 0.146724554, 0.166075038, 0.185425523, 0.204776008, ...
