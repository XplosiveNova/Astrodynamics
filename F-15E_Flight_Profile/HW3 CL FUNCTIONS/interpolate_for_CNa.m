            3.967605802, 3.95715825, 3.944811142, 3.93008959, 3.913231039, 3.896372488, ...
            3.878801604, 3.860280943, 3.841047948, 3.822289842, 3.803056847, 3.782874075, ...
            3.761741525, 3.742033641, 3.71971387, 3.697868987, 3.666126537];

    % Compute input
     tanLE_Beta = tan(Lambda_LE) / beta;

    % Interpolated output
    betaCNa = interp1(tanLE_Beta_data, betaCNa_data, tanLE_Beta, 'linear', 'extrap');

    % Compute final output
    CNa = betaCNa / beta;
end